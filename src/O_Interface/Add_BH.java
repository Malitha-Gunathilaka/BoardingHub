    public void AutoID ()
    {
        
        try {
            Statement s= con.createStatement();
            rs = s.executeQuery("select Max(BID) from boardinghouse");
            rs.next();
            rs.getString("Max(BID)");
            
            
            if(rs.getString("Max(BID)")==null)
            {
            
                BHI.setText("BHI001");
                
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(BID)").substring(3,rs.getString("Max(BID)").length()));
                id++;
                BHI.setText("BHI"+String.format("%03d", id));
                
                
            }
            
        } catch (SQLException ex) { 
            //java.util.logging.Logger.getLogger(ReTable.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    
    }

