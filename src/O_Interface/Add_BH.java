            ps.setBytes(11,photo1 );
            ps.setBytes(12,photo2 );
            ps.setBytes(13,photo3 );
            ps.setBytes(14,photo4 );

            int rs1 = ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Boarding House Register Succesfully");
            
            Add_BH bh =new Add_BH(Nic);
            Add_BHInterface.removeAll();
